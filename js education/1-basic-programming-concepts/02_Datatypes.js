/**

 In JavaScript, there are several data types that can be used to represent different kinds of values.

 Numbers: Numeric values, such as integers or decimals.
 Strings: Text values, enclosed in quotation marks.
 Booleans: Logical values, either true or false.
 Undefined: A special value that represents the absence of a value.
 Null: A special value that represents the intentional absence of any object value.
 Objects: Used to store collections of data or more complex entities.
 Arrays: A specific type of object used to store ordered collections of data.

 Here are some examples of how these data types can be used in JavaScript:
 */

// Numbers
let x = 5;
let y = 3.14;

// Strings
let message = "Hello, world!";
let name = "John Doe";

// Booleans
let isRaining = true;
let isLoggedIn = false;

// Undefined
let someValue; // not assigned a value yet, so it's undefined

// Null
let nothing = null; // intentionally set to no value

// Objects
let person = {
    name: "Jane Doe",
    age: 25,
    email: "jane@example.com"
};

// Arrays
let numbers = [1, 2, 3, 4, 5];
let fruits = ["apple", "banana", "orange"];

/**

 Understanding the different data types in JavaScript is important for writing effective and efficient code.
 By using the appropriate data type for each variable or value, we can ensure that our code is easy to read,
 understand, and maintain. Additionally, knowing how to work with different data types enables us to create
 more complex programs that can handle a wider variety of inputs and produce more sophisticated outputs.
 */